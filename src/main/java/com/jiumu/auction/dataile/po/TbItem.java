package com.jiumu.auction.dataile.po;


public class TbItem {

  private long itemId;
  private String itemName;
  private long itemTreeId;


  public long getItemId() {
    return itemId;
  }

  public void setItemId(long itemId) {
    this.itemId = itemId;
  }


  public String getItemName() {
    return itemName;
  }

  public void setItemName(String itemName) {
    this.itemName = itemName;
  }


  public long getItemTreeId() {
    return itemTreeId;
  }

  public void setItemTreeId(long itemTreeId) {
    this.itemTreeId = itemTreeId;
  }

}
