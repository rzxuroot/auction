package com.jiumu.auction.recharge.vo;

public class PayInfo {
    private String InfoName;
    private long InfoPrice;
    private String InfoNo;

    public String getInfoName() {
        return InfoName;
    }

    public void setInfoName(String infoName) {
        InfoName = infoName;
    }

    public long getInfoPrice() {
        return InfoPrice;
    }

    public void setInfoPrice(long infoPrice) {
        InfoPrice = infoPrice;
    }

    public String getInfoNo() {
        return InfoNo;
    }

    public void setInfoNo(String infoNo) {
        InfoNo = infoNo;
    }
}
